def set_seed(seed=42):
    random.seed(seed)                # Python random
    np.random.seed(seed)             # NumPy
    torch.manual_seed(seed)          # PyTorch (CPU)
    torch.cuda.manual_seed(seed)     # PyTorch (GPU)
    torch.cuda.manual_seed_all(seed) # Multi-GPU

    torch.backends.cudnn.deterministic = True
    torch.backends.cudnn.benchmark = False

    torch.use_deterministic_algorithms(True, warn_only=True)

    print(f"Seed set to {seed}")

class ShanghaiTechDataset(Dataset):
    MEAN   = [0.485, 0.456, 0.406]
    STD    = [0.229, 0.224, 0.225]
    CROP   = 512
    STRIDE = 8

    def __init__(self, part='A', split='train'):
        self.split   = split
        self.h5_dir  = f"./ShanghaiTech/part_{part}/{split}_data/ground-truth-h5"
        img_dir      = f"./ShanghaiTech/part_{part}/{split}_data/images"

        all_imgs = sorted(glob.glob(os.path.join(img_dir, "*.jpg")))
        self.img_paths = [
            p for p in all_imgs
            if os.path.exists(os.path.join(self.h5_dir, Path(p).stem + ".h5"))
        ]
        assert len(self.img_paths) > 0, \
            f"No pairs found. img_dir={img_dir}  h5_dir={self.h5_dir}"
        print(f"Part {part} {split}: {len(self.img_paths)} pairs")
        self.tf = T.Compose([
            T.ToTensor(),
            T.Normalize(self.MEAN, self.STD)
        ])

    def __len__(self):
        return len(self.img_paths)

    def __getitem__(self, idx):
        stem    = Path(self.img_paths[idx]).stem
        img     = cv2.cvtColor(cv2.imread(self.img_paths[idx]), cv2.COLOR_BGR2RGB)
        with h5py.File(os.path.join(self.h5_dir, stem + ".h5"), 'r') as f:
            density = np.asarray(f['density'], dtype=np.float32)

        h, w    = img.shape[:2]
        scale   = self.CROP / min(h, w)
        nh, nw  = max(self.CROP, int(h*scale)), max(self.CROP, int(w*scale))
        img     = cv2.resize(img,     (nw, nh), interpolation=cv2.INTER_LINEAR)
        density = cv2.resize(density, (nw, nh), interpolation=cv2.INTER_LINEAR)
        density *= (h * w) / (nh * nw)

        if self.split == 'train':
            top  = random.randint(0, nh - self.CROP)
            left = random.randint(0, nw - self.CROP)
            if random.random() > 0.5:
                img     = np.fliplr(img).copy()
                density = np.fliplr(density).copy()
        else:
            top  = (nh - self.CROP) // 2
            left = (nw - self.CROP) // 2

        img     = img[top:top+self.CROP, left:left+self.CROP]
        density = density[top:top+self.CROP, left:left+self.CROP]

        out     = self.CROP // self.STRIDE
        den_out = cv2.resize(density, (out, out),
                             interpolation=cv2.INTER_LINEAR) * self.STRIDE**2

        return self.tf(img), torch.from_numpy(den_out).unsqueeze(0).float()

