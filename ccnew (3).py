class ChannelAttention(nn.Module):
    def __init__(self, channels, reduction=16):
        super().__init__()
        mid      = max(channels // reduction, 8)
        self.avg = nn.AdaptiveAvgPool2d(1)
        self.max = nn.AdaptiveMaxPool2d(1)
        self.fc  = nn.Sequential(
            nn.Conv2d(channels, mid, 1, bias=False),
            nn.ReLU(inplace=True),
            nn.Conv2d(mid, channels, 1, bias=False),
        )
        self.sig = nn.Sigmoid()
    def forward(self, x):
        return self.sig(self.fc(self.avg(x)) + self.fc(self.max(x)))

class SpatialAttention(nn.Module):
    def __init__(self):
        super().__init__()
        self.conv = nn.Conv2d(2, 1, 7, padding=3, bias=False)
        self.sig  = nn.Sigmoid()
    def forward(self, x):
        avg = x.mean(dim=1, keepdim=True)
        mx  = x.max(dim=1,  keepdim=True).values
        return self.sig(self.conv(torch.cat([avg, mx], dim=1)))

class CBAM(nn.Module):
    def __init__(self, channels):
        super().__init__()
        self.ca = ChannelAttention(channels)
        self.sa = SpatialAttention()
    def forward(self, x):
        return x * self.ca(x) * self.sa(x)

def dilated_block(in_ch, out_ch, d=2):
    return nn.Sequential(
        nn.Conv2d(in_ch, out_ch, 3, padding=d, dilation=d, bias=False),
        nn.BatchNorm2d(out_ch),
        nn.ReLU(inplace=True),
    )

class CrowdCountingNet(nn.Module):
    def __init__(self, pretrained=True):
        super().__init__()
        vgg            = models.vgg16_bn(weights='DEFAULT' if pretrained else None)
        self.frontend  = nn.Sequential(*list(vgg.features.children())[:33])
        self.backend   = nn.Sequential(
            dilated_block(512, 512, 1),
            dilated_block(512, 512, 2),
            dilated_block(512, 512, 2),
            dilated_block(512, 256, 2),
            dilated_block(256, 128, 2),
            dilated_block(128,  64, 2),
        )
        self.attention   = CBAM(64)
        self.output_conv = nn.Sequential(
            nn.Conv2d(64, 1, 1),
            nn.ReLU(inplace=True),
        )
        self._init_weights()

    def _init_weights(self):
        for m in list(self.backend.modules()) + list(self.output_conv.modules()):
            if isinstance(m, nn.Conv2d):
                nn.init.kaiming_normal_(m.weight, nonlinearity='relu')
            elif isinstance(m, nn.BatchNorm2d):
                nn.init.ones_(m.weight)
                nn.init.zeros_(m.bias)

    def forward(self, x):
        x = self.frontend(x)
        x = self.backend(x)
        x = self.attention(x)
        return self.output_conv(x)


