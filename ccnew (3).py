for epoch in range(1, EPOCHS + 1):

    model.train()
    total_loss = 0
    for imgs, dens in train_loader:
        imgs, dens = imgs.to(device), dens.to(device)
        optimizer.zero_grad()
        with autocast():
            pred = model(imgs)
            loss = criterion(pred, dens)
        scaler.scale(loss).backward()
        scaler.unscale_(optimizer)
        torch.nn.utils.clip_grad_norm_(model.parameters(), 5.0)
        scaler.step(optimizer)
        scaler.update()
        total_loss += loss.item()
    scheduler.step()

    if epoch % 5 == 0 or epoch == 1:
        model.eval()
        mae, mse, n = 0.0, 0.0, 0
        with torch.no_grad():
            for imgs, dens in test_loader:
                imgs, dens = imgs.to(device), dens.to(device)
                with autocast():
                    pred = model(imgs)
                diff  = abs(pred.sum().item() - dens.sum().item())
                mae  += diff
                mse  += diff ** 2
                n    += 1
        mae  = mae / n
        rmse = (mse / n) ** 0.5
        if mae < best_mae:
            best_mae = mae
            torch.save(model.state_dict(), 'checkpoints/best_model.pth')
        print(f"{epoch:>5} | {total_loss/len(train_loader):>10.4f} "
              f"| {mae:>8.2f} | {rmse:>8.2f} | {best_mae:>8.2f}")
    else:
        print(f"  epoch {epoch}/{EPOCHS}  loss={total_loss/len(train_loader):.4f}", end='\r')

print(f"\nDone. Best MAE = {best_mae:.2f}")

